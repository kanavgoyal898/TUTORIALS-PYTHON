# Integers & Floats: Working with Numeric Data

# Arithmetic Operations
# Addition
print(f"3 + 2  = {3+2}")
# Subtraction
print(f"3 - 2  = {3-2}")
# Multiplication
print(f"3 * 2  = {3*2}")
# Division
print(f"3 / 2  = {3/2}")
# Modulus
print(f"3 % 2  = {3%2}")
# Floor Division
print(f"3 // 2 = {3//2}")
# Exponent
print(f"3 ** 2 = {3**2}")

# Assignment Operations
# Assignment operator
num = 13
print(f"num = {num}")
# Additive-Assignment operator
num += 1
print(f"num after += {num}")
# Subtractive-Assignment operator
num -= 1
print(f"num after -= {num}")
# Multiplicative-Assignment operator
num *= 2
print(f"num after *= {num}")
# Division-Assignemnt operator
num /= 2
print(f"num after /= {num}")

# Comparison Operators
# equals to operator
print(f"2 == 3 : {2==3}")
# NOT equals to operator
print(f"2 != 3 : {2!=3}")
# greater than operator
print(f"2 > 3  : {2 > 3}")
# lesser than operator
print(f"2 < 3  : {2 < 3}")
# greater than or equals to operator
print(f"2 >= 3 : {2 > 3}")
# lesser than or equals to operator
print(f"2 <= 3 : {2 < 3}")

# Built-In Functions
# abs()
print(f"abs(13)  =  {abs(13)}")
print(f"abs(-13) =  {abs(-13)}")
# round()
print(f"round(3.141592)    = {round(3.141592)}")
print(f"round(3.141592, 1) = {round(3.141592, 1)}")
print(f"round(3.141592, 2) = {round(3.141592, 2)}")
print(f"round(3.141592, 3) = {round(3.141592, 3)}")
print(f"round(3.141592, 4) = {round(3.141592, 4)}")
print(f"round(3.141592, 5) = {round(3.141592, 5)}")
# int()
val = '13'
print(f"val = {val} | type(val) = {type(val)}")
val = int(val)
print(f"val = {val} | type(val) = {type(val)}")